# Exemplo de listas e dicionários em Python

# Lista
frutas = ["laranja", "uva", "maça"]
frutas.append("banana")
print("Lista de frutas:", frutas)

# Dicionário
aluno = {
    "nome": "Janaina",
    "idade": 30,
    "curso": "Python"
}
print("Nome do aluno:", aluno["nome"])
print("Idade:", aluno["idade"])
print("Curso:", aluno["curso"])
